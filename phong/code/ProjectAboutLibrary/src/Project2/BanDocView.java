    private void InsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertButtonActionPerformed
        // TODO add your handling code here:
        setValidateIntoTable();

//        int IdBanDoc = Integer.parseInt(maBanDoc.getText());
        String TenBanDoc = txt_tenBanDoc.getText();
        String DiaChi = txt_diaChi.getText();
        String NgaySinh = txt_ngaySinh.getText();
        String sDT = txt_SDT.getText();
        String LoaiBanDoc = null;
        if (RadioButtonSV.isSelected()) {
            LoaiBanDoc = RadioButtonSV.getText();
        }
        if (RadioButtonHS.isSelected()) {
            LoaiBanDoc = RadioButtonHS.getText();
        }

        condition();

        //add vao co so du lieu
        if (condition == true) {
            try {
                BanDocDB.addData(TenBanDoc, DiaChi, NgaySinh, sDT, LoaiBanDoc);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(BanDocView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(BanDocView.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (condition == false) {
            JOptionPane.showMessageDialog(this, "Thêm bạn đọc thất bại");
            condition = true;
            return;
        }

        ShowResult();
    }//GEN-LAST:event_InsertButtonActionPerformed
