    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
//      int IdBanDoc = Integer.parseInt(maBanDoc.getText());
        String TenBanDoc = txt_tenBanDoc.getText();
        String DiaChi = txt_diaChi.getText();
        String NgaySinh = txt_ngaySinh.getText();
        String sDT = txt_SDT.getText();
        String LoaiBanDoc = null;
        //id duoc chon tren table
        condition();
        int row = Table.getSelectedRow();
//        TableModel model= Table.getModel();
        String IdBanDoc = (Table.getModel().getValueAt(row, 0).toString());
        //chon 1 trong 2 group button

        if (RadioButtonSV.isSelected()) {
            LoaiBanDoc = RadioButtonSV.getText();
        }
        if (RadioButtonHS.isSelected()) {
            LoaiBanDoc = RadioButtonHS.getText();
        }
        //truyen tham so vao trong ham updateData
        if (condition == true) {
            BanDocDB.updateData(Integer.parseInt(IdBanDoc), TenBanDoc, DiaChi, NgaySinh, sDT, LoaiBanDoc);
        } else if (condition == false) {
            JOptionPane.showMessageDialog(this, "Sửa tài khoản thất bại");
            condition = true;
            return;
        }
        ShowTableInfo();
    }//GEN-LAST:event_UpdateButtonActionPerformed

